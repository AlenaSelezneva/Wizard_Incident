#include "LevelsTilesSchema.h"

std::vector<std::vector<Tile::Type>> LevelsTilesSchema::getLevelLayout(Level l)
{
    switch (l)
    {
    case Level::Dangeon:
        return getDangeonLevelLayout();
        break;
    case Level::First:
        return getFirstLevelLayout();
        break;
    case Level::Second:
        return getSecondLevelLayout();
        break;
    default:
        break;
    }
    return std::vector<std::vector<Tile::Type>>();
}


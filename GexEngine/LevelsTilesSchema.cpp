std::vector<std::vector<Tile::Type>> LevelsTilesSchema::IntVectorToTileType(std::vector<std::vector<int> > input, int n, int m) {
    std::vector<std::vector<Tile::Type>> res(n, std::vector<Tile::Type>(m));
    for (int i = 0; i < n; ++i)
        for (int j = 0; j < m; ++j)
            res[i][j] = Tile::Type(input[i][j]);

    return res;
}